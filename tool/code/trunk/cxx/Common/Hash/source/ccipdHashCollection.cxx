



//////////////////////////////////////////////////////////////////////////////////////////
#include "ccipdHashCollection.h"

#include <boost/functional/hash.hpp>  // for combining hash values
//////////////////////////////////////////////////////////////////////////////////////////



namespace ccipd
{
  
  

//////////////////////////////////////////////////////////////////////////////////////////
HashValueType GetHashValue( const SortedHashCollectionType & sortedHashes )
{

  // initialize
  const HashValueType seed = 0;
  HashValueType hashValue = seed;

  // combine all the sorted hash values together
  boost::hash_range( hashValue, sortedHashes.cbegin(), sortedHashes.cend() );

  return hashValue;

}
//////////////////////////////////////////////////////////////////////////////////////////



} // namespace
