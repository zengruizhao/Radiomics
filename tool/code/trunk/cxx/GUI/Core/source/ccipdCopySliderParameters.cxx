void CopySliderParameters(QSlider &oldSlider, QSlider &newSlider )
{
  newSlider.setMinimum ( oldSlider.minimum()  );
  newSlider.setMaximum ( oldSlider.maximum()  );
  newSlider.setValue   ( oldSlider.value()    );  
  newSlider.setPageStep( oldSlider.pageStep() );
}
